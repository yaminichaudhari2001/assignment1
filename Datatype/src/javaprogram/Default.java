package javaprogram;

public class Default {

    static short a = 0;
    static int b;
    static  long c;
    static float d;
    static double e;
    static char f;
    static  String s;
    static boolean m;
    public static void main(String[] args) {
        System.out.println("This is default value of primitive data type");

        System.out.println("The default value of short " + a);
        System.out.println("The default value of integer " + b);
        System.out.println("The default value of long " + c);
        System.out.println("The default value of float " + d);
        System.out.println("The default value of double " + e);
        System.out.println("The default value of char " + f);
        System.out.println("The default value of string " + s);
        System.out.println("The default value of boolean " + m);


    }
}
